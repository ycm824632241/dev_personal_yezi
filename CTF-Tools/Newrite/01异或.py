def xor_binary_strings(s1: str, s2: str) -> str:
    """
    对两个01字符串进行异或运算（自动忽略空格和换行符\n），返回结果01字符串

    参数:
        s1: 第一个01字符串（可包含空格、换行符，仅有效字符为'0'和'1'）
        s2: 第二个01字符串（可包含空格、换行符，仅有效字符为'0'和'1'）

    返回:
        异或后的01字符串

    异常:
        ValueError: 若过滤后有效01字符长度不一致，或包含非01/非空格/非换行符的非法字符
    """
    # 步骤1：过滤空格和换行符（保留仅'0'和'1'的有效字符）
    # 需过滤的字符集合：空格 ' ' 和换行符 '\n'
    filtered_s1 = [c for c in s1 if c not in (' ', '\n')]
    filtered_s2 = [c for c in s2 if c not in (' ', '\n')]

    # 步骤2：校验过滤后的字符是否仅包含'0'和'1'（排除其他非法字符）
    valid_chars = {'0', '1'}
    invalid_chars_s1 = set(filtered_s1) - valid_chars
    invalid_chars_s2 = set(filtered_s2) - valid_chars
    invalid_chars = invalid_chars_s1.union(invalid_chars_s2)

    if invalid_chars:
        raise ValueError(
            f"输入字符串包含非法字符！仅允许'0'、'1'、空格和换行符，非法字符：{sorted(invalid_chars)}"
        )

    # 步骤3：校验过滤后的有效长度是否一致
    if len(filtered_s1) != len(filtered_s2):
        raise ValueError(
            f"过滤空格和换行符后的有效01字符长度不一致！s1有效长度：{len(filtered_s1)}，s2有效长度：{len(filtered_s2)}"
        )

    # 步骤4：逐位异或（基于过滤后的有效字符）
    result = [str(int(c1) ^ int(c2)) for c1, c2 in zip(filtered_s1, filtered_s2)]
    return ''.join(result)


