from sympy import mod_inverse
from Crypto.Util.number import long_to_bytes

def chinese_remainder_theorem(n, a):
    sum = 0
    prod = 1
    for ni in n:
        prod *= ni

    for ni, ai in zip(n, a):
        p = prod // ni
        sum += ai * mod_inverse(p, ni) * p

    return sum % prod


def find_root(e, x):
    # 计算整数x的1/e次幂根
    high = 1
    while high ** e < x:
        high *= 2
    low = high // 2
    while low < high:
        mid = (low + high) // 2
        if mid ** e < x:
            low = mid + 1
        else:
            high = mid
    return low


def decrypt_rsa(ciphertexts, moduli, e):
    # 使用中国剩余定理解密RSA
    M = chinese_remainder_theorem(moduli, ciphertexts)

    # 尝试计算M的e次方根，这假设M小于所有n的乘积
    m = find_root(e, M)
    return m


